#include <string.h>

size_t	ft_strlcat(char *s1, const char *s2, size_t n)
{
	size_t i;

	i = 0;
	while (*s1 != '\0' && i < n - 1)
	{
		s1++;
		i++;
	}
	while (*s2 != '\0' && i < n - 1)
	{
		*s1++ = *s2++;
		i++;
	}
	if (*s2 != '\0')
		return (n);
	*s1 = '\0';
	return (i);
}
