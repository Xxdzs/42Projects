#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int a;

	a = -1;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	*range = (int *)malloc((max - min) * sizeof(int));
	if (*range == NULL)
		return (0);
	while (++a < max - min)
		range[0][a] = min + a;
	return (max - min);
}
